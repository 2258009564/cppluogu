// 通用版本的 >> 重载，用于任意类型的 std::vector<T>
template <typename T>
istream &operator>>(istream &in, vector<T> &v)
{
    for (auto &x : v)
    {
        in >> x;
    }
    return in;
}

// 通用版本的 << 重载，用于任意类型的 std::vector<T>
template <typename T>
ostream &operator<<(ostream &out, const vector<T> &v)
{
    for (const auto &x : v)
    {
        out << x << ' ';
    }
    return out;
}

signed main()
{
    BUFF;
    int H, T, n;
    cin >> H >> T >> n;
    vi h(n), t(n), k(n);

    for (int i = 0; i < n; i++)
    {
        cin >> h[i] >> t[i] >> k[i];
    }

    vvi dp(H + 1, vi(T + 1, 0));

    for (int i = 0; i < n; i++)
    {
        for (int vol = H; vol >= h[i]; vol--)
        {
            for (int wei = T; wei >= t[i]; wei--)
            {
                dp[vol][wei] = max(dp[vol][wei], dp[vol - h[i]][wei - t[i]] + k[i]);
            }
        }
    }
    cout << dp[H][T];
}